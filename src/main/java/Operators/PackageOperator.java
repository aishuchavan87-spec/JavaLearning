package Operators;

public class PackageOperator {
    public static void main (String[] args) {

        int i=10;
        int j=8;

        System.out.println(i==j);
        System.out.println(i!=j);
        System.out.println(i>=j);
        System.out.println(i<=j);
        System.out.println(i>j);
        System.out.println(i<j);
    }
}
